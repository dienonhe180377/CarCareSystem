package entity;

import java.util.Date;

/**
 *
 * @author Admin
 */
public class Attendance {
    private int id;
    private User user;
    private Date date;
    private boolean status;

    public Attendance(int id, User user, Date date, boolean status) {
        this.id = id;
        this.user = user;
        this.date = date;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }
    
}
