package entity;

/**
 *
 * @author Admin
 */
public class Supplier {
    private int id;
    private String name;
    private String logo;
    private String description;
    private String email;
    private String phone;
    private String address;

    public Supplier(int id, String name, String logo, String description, String email, String phone, String address) {
        this.id = id;
        this.name = name;
        this.logo = logo;
        this.description = description;
        this.email = email;
        this.phone = phone;
        this.address = address;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    @Override
    public String toString() {
        return "Supplier{" + "id=" + id + ", name=" + name + ", logo=" + logo + ", description=" + description + ", email=" + email + ", phone=" + phone + ", address=" + address + '}';
    }

    
}
